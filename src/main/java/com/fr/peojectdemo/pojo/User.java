package com.fr.peojectdemo.pojo;


public class User {
	private String username;//用户名
	private String password;//密码
	private String uid;//用户识别码
	private String phonenumber;//手机 
	private String email;//邮箱 
	private String icon;//头像
	private int investmentage;//投资年龄 
	private String profile;//个人简介 
	private String profession;//职业 
	private String residence;//居住地 
	private int privacy;//隐私识别码 
	private int state;//状态码 
	private String code;//激活码 
	private Blog blog;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getPhonenumber() {
		return phonenumber;
	}
	public void setPhonenumber(String phonenumber) {
		this.phonenumber = phonenumber;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public int getInvestmentage() {
		return investmentage;
	}
	public void setInvestmentage(int investmentage) {
		this.investmentage = investmentage;
	}
	public String getProfile() {
		return profile;
	}
	public void setProfile(String profile) {
		this.profile = profile;
	}
	public String getProfession() {
		return profession;
	}
	public void setProfession(String profession) {
		this.profession = profession;
	}
	public String getResidence() {
		return residence;
	}
	public void setResidence(String residence) {
		this.residence = residence;
	}
	public int getPrivacy() {
		return privacy;
	}
	public void setPrivacy(int privacy) {
		this.privacy = privacy;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public Blog getBlog() {
		return blog;
	}
	public void setBlog(Blog blog) {
		this.blog = blog;
	}
	public User(String username, String password, String uid, String phonenumber, String email, String icon,
			int investmentage, String profile, String profession, String residence, int privacy, int state, String code,
			Blog blog) {
		super();
		this.username = username;
		this.password = password;
		this.uid = uid;
		this.phonenumber = phonenumber;
		this.email = email;
		this.icon = icon;
		this.investmentage = investmentage;
		this.profile = profile;
		this.profession = profession;
		this.residence = residence;
		this.privacy = privacy;
		this.state = state;
		this.code = code;
		this.blog = blog;
	}
	public User() {
		super();
	}
	@Override
	public String toString() {
		return "User [username=" + username + ", password=" + password + ", uid=" + uid + ", phonenumber=" + phonenumber
				+ ", email=" + email + ", icon=" + icon + ", investmentage=" + investmentage + ", profile=" + profile
				+ ", profession=" + profession + ", residence=" + residence + ", privacy=" + privacy + ", state="
				+ state + ", code=" + code + ", blog=" + blog + "]";
	}
	
	
}
